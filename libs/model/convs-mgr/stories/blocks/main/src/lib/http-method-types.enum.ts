/**
 * When creating and fetching  variables, different Http Methods are needed
 */
export enum HttpMethodTypes {
  /**
   * Method that makes a HTTP Post request to the API
   */
  POST = 1,
  /**
     * Method that makes a HTTP fetch request to the API
     */
  GET = 2,
  /**
     * Method that makes a Delete request to the API
     */
  DELETE = 3

}